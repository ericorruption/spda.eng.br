            <h2 class="article-title" id="quem-somos"><span>Quem somos?</span></h2>

            <p>O nome SPDA, que começou de uma forma curiosa, se tornou parte da nossa filosofia.</p>

            <p>Ainda dentro de uma grande empresa, o sócio-fundador da SPDA construiu uma reputação de encarar e resolver qualquer trabalho, até os que chegavam abaixo de mau tempo.</p>

            <p>SPDA significa "Sistema de Proteção contra Descargas Atmosféricas", vulgo "para-raios". Esse nome é uma representação exata para o que buscamos em nosso trabalho.</p>

            <p class="illustration-paragraph">Acreditamos que <em>trabalho cuidadoso</em> durante a fase de projeto <em>reduz a probabilidade de problemas</em> na execução da obra, provém <em>melhor controle de custos</em> e garante <em>qualidade superior</em> no produto final. Além disso, o mesmo trabalho de qualidade nos permite propor soluções técnicas elegantes, seguras e funcionais.</p>
